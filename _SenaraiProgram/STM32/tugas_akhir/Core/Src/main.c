void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim){
    if (htim == &htim3){
		HAL_I2C_Mem_Read(&hi2c1, AS5600_ADDRESS << 1, AS5600_RAW_ADD_1, 1,
				&as5600_buff_[0][0], 1, HAL_MAX_DELAY);
		HAL_I2C_Mem_Read(&hi2c1, AS5600_ADDRESS << 1, AS5600_RAW_ADD_2, 1,
				&as5600_buff_[0][1], 1, HAL_MAX_DELAY);
		raw_angle_buff_[0] = as5600_buff_[0][1] << 8 | as5600_buff_[0][0];


		HAL_I2C_Mem_Read(&hi2c2, AS5600_ADDRESS << 1, AS5600_RAW_ADD_1, 1,
					&as5600_buff_[1][0], 1, HAL_MAX_DELAY);
		HAL_I2C_Mem_Read(&hi2c2, AS5600_ADDRESS << 1, AS5600_RAW_ADD_2, 1,
				&as5600_buff_[1][1], 1, HAL_MAX_DELAY);
		raw_angle_buff_[1] = as5600_buff_[1][1] << 8 | as5600_buff_[1][0];


		HAL_I2C_Mem_Read(&hi2c3, AS5600_ADDRESS << 1, AS5600_RAW_ADD_1, 1,
				&as5600_buff_[2][0], 1, HAL_MAX_DELAY);
		HAL_I2C_Mem_Read(&hi2c3, AS5600_ADDRESS << 1, AS5600_RAW_ADD_2, 1,
				&as5600_buff_[2][1], 1, HAL_MAX_DELAY);
		raw_angle_buff_[2] = as5600_buff_[2][1] << 8 | as5600_buff_[2][0];


		data.FSR_value = (uint16_t)(dma_value[0] & 0xFFFF);
		data.link_angle_1 = raw_angle_buff_[2];
		data.link_angle_2 = raw_angle_buff_[1];
		data.link_angle_3 = raw_angle_buff_[0];
		data.link_angle_4 = (uint16_t)(dma_value[3] & 0xFFFF);
		data.link_angle_5 = (uint16_t)(dma_value[2] & 0xFFFF);
		data.link_angle_6 = (uint16_t)(dma_value[1] & 0xFFFF);
    }
}

